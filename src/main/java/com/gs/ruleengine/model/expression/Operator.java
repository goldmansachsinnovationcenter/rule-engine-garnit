package com.gs.ruleengine.model.expression;

/**
 * Enum for comparison operators used in conditions.
 */
public enum Operator {
    EQUALS,
    NOT_EQUALS,
    GREATER_THAN,
    GREATER_THAN_OR_EQUALS,
    LESS_THAN,
    LESS_THAN_OR_EQUALS,
    CONTAINS,
    STARTS_WITH,
    ENDS_WITH,
    IS_NULL,
    IS_NOT_NULL
}
